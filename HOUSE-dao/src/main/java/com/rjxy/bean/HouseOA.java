package com.rjxy.bean;

public class HouseOA {
	private String title;
	private String mixPrice;
	private String maxPrice;
	private String districtId;
	private String streetId;
	private String typeId;
	private String mixFloorage;
	private String maxFloorage;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getMixPrice() {
		return mixPrice;
	}

	public void setMixPrice(String mixPrice) {
		this.mixPrice = mixPrice;
	}

	public String getMaxPrice() {
		return maxPrice;
	}

	public void setMaxPrice(String maxPrice) {
		this.maxPrice = maxPrice;
	}

	
	public String getDistrictId() {
		return districtId;
	}

	public void setDistrictId(String districtId) {
		this.districtId = districtId;
	}

	public String getStreetId() {
		return streetId;
	}

	public void setStreetId(String streetId) {
		this.streetId = streetId;
	}

	public String getTypeId() {
		return typeId;
	}

	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}

	public String getMixFloorage() {
		return mixFloorage;
	}

	public void setMixFloorage(String mixFloorage) {
		this.mixFloorage = mixFloorage;
	}

	public String getMaxFloorage() {
		return maxFloorage;
	}

	public void setMaxFloorage(String maxFloorage) {
		this.maxFloorage = maxFloorage;
	}

}
